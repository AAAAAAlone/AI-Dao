export const getShiIndex = (binary: string) => {
  const up = binary.slice(0, 3), lo = binary.slice(3, 6);
  if (up === lo) return 5; 
  const bits = binary.split('').map(Number).reverse();
  if (bits[0] !== bits[3] && bits[1] === bits[4] && bits[2] === bits[5]) return 0;
  if (bits[0] !== bits[3] && bits[1] !== bits[4] && bits[2] === bits[5]) return 1;
  if (bits[0] !== bits[3] && bits[1] !== bits[4] && bits[2] !== bits[5]) return 2;
  if (bits[0] === bits[3] && bits[1] !== bits[4] && bits[2] !== bits[5]) return 3;
  if (bits[0] === bits[3] && bits[1] === bits[4] && bits[2] !== bits[5]) return 4;
  return 2;
};

export const getRelative = (palaceEl: string, lineEl: string) => {
  const FIVE_ELEMENTS: Record<string, string> = { '金': '水', '水': '木', '木': '火', '火': '土', '土': '金' };
  const FIVE_ELEMENTS_KEO: Record<string, string> = { '金': '木', '木': '土', '土': '水', '水': '火', '火': '金' };
  if (palaceEl === lineEl) return '兄';
  if (FIVE_ELEMENTS[palaceEl] === lineEl) return '孙';
  if (FIVE_ELEMENTS[lineEl] === palaceEl) return '父';
  if (FIVE_ELEMENTS_KEO[palaceEl] === lineEl) return '财';
  return '官';
};

